public class InvalidFlightNameException extends Exception{

	public InvalidFlightNameException() {
		super();
		
	}

	public InvalidFlightNameException(String arg0) {
		super(arg0);
		
	}

	public InvalidFlightNameException(Throwable arg0) {
		super(arg0);
		
	}
	

}
