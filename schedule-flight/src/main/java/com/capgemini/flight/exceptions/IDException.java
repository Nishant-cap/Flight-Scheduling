package com.capgemini.flight.exceptions;
/*************************************************************************************************************
 * 
 * @author Nishant Shrivastav
 * @Description Exception class for ID
 *************************************************************************************************************/
public class IDException extends Exception {

	public IDException(String arg0) {
		super(arg0);
	}

	public IDException(Throwable arg0) {
		super(arg0);
		
	}


	public IDException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}
	
	
}
